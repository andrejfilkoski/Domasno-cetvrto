public class Restoran {

    private String ime, lokacija;
    private int telefonskiBroj, brojNaSedista;

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getLokacija() {
        return lokacija;
    }

    public void setLokacija(String lokacija) {
        this.lokacija = lokacija;
    }

    public int getTelefonskiBroj() {
        return telefonskiBroj;
    }

    public void setTelefonskiBroj(int telefonskiBroj) {
        this.telefonskiBroj = telefonskiBroj;
    }

    public int getBrojNaSedista() {
        return brojNaSedista;
    }

    public void setBrojNaSedista(int brojNaSedista) {
        this.brojNaSedista = brojNaSedista;
    }
}
