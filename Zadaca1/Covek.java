public class Covek {
    String ime;
    String prezime;
    Long maticen;

    public Covek() {
    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getPrezime() {
        return prezime;
    }

    public void setPrezime(String prezime) {
        this.prezime = prezime;
    }

    public Long getMaticen() {
        return maticen;
    }

    public void setMaticen(Long maticen) {
        this.maticen = maticen;
    }
}
